def greet():
    print("Hello")
    print("Good morning")
    print("Have a great day")

greet()

def greet_with_name(name):
    print(f"Hello Good morning {name} ")
    print(f"Have a great day {name}")

greet_with_name("Nishank")


def greet_with(name, location):
    print(f"Hello Good morning {name} and welcome to {location}")

greet_with("Nishank","Pune")